    private void botoes() {
        if(dlm.getSize() == 0 || jList1.getSelectedIndex() == -1) {
            removerCliente.setEnabled(false);
            editarCliente.setEnabled(false);
        } else {
            removerCliente.setEnabled(true);
            editarCliente.setEnabled(true);
        }
    }
    
    public static void listaAtualizada() {
        Clientes.listaClientes.clear();
        
        try {
            Cliente.lista_cliente = Cliente.getLista( CriaArquivo.read("cliente") );
        } catch (IOException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        Clientes.listaClientes.addAll(Cliente.lista_cliente.values());
    }
    
    public static void listaAtualizada(Cliente c) {
        Clientes.listaClientes.clear();
        Clientes.listaClientes.add(c);
    }
    
    public static void atualizaLista() {
        
        try {
            Cliente.lista_cliente = Cliente.getLista( CriaArquivo.read("cliente") );
        } catch (IOException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        dlm.removeAllElements();
        
        listaClientes.forEach((user) -> {
            dlm.addElement(user.getNome() + " - " + user.getCPF());
        });
        
    }
        

