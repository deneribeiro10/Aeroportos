    private void botaoPesquisaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisaClienteActionPerformed
        
        
        if(Cliente.lista_cliente.containsKey( pesquisaClienteTxt.getText() )) {
            
            dlm.removeAllElements();
            dlm.addElement( Cliente.lista_cliente.get( pesquisaClienteTxt.getText() ).getNome()
            + " - " 
            + Cliente.lista_cliente.get( pesquisaClienteTxt.getText() ).getCPF());
            
            Clientes.listaAtualizada(Cliente.lista_cliente.get( pesquisaClienteTxt.getText() ));
            
            this.notFound.setVisible(false);
            
        } else {
            
            dlm.removeAllElements();
            this.notFound.setVisible(true);
        }
        
    }//GEN-LAST:event_botaoPesquisaClienteActionPerformed

