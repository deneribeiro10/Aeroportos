    private void removerClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerClienteActionPerformed
        
        int index = jList1.getSelectedIndex(); 
        int tamanho = dlm.getSize();
        
        String id = listaClientes.get(index).getCPF();
        
        if(0 !=  JOptionPane.showConfirmDialog(null, 
                "Um cliente ser√° removido do sistema.\n Deseja continuar? ", 
                "Remover cliente", 
                JOptionPane.YES_NO_OPTION
        )) {
            return;
        }
        
        dlm.remove(index); 
        listaClientes.remove(index);
        
        if(index == tamanho-1) {
            index--;
        }
        jList1.setSelectedIndex(index);
        jList1.ensureIndexIsVisible(index);
        
        try {
            Cliente.removeCliente(id);
        } catch (IOException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FaltaObjetosException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Clientes.listaAtualizada();
        Clientes.atualizaLista();
        
        
    }//GEN-LAST:event_removerClienteActionPerformed

