    public void setNomeTelEndCPF(String nome, String telefone, String endereco, String cpf) {
        this.nomeCliente.setText(nome);
        this.telefoneCliente.setText(telefone);
        this.enderecoCliente.setText(endereco);
        this.cpfCliente.setText(cpf);
    }
    
    public int criaCliente() {
        Object[] jOpcoes = {"N√£o", "Sim"};
        String tittle = "Cadastro de Clientes";
        String texto = "Cadastro efetuado com sucesso! \nDeseja cadastrar novo cliente? ";
        
        try {
            Cliente.criaCliente(nomeCliente.getText(),telefoneCliente.getText(),enderecoCliente.getText(),cpfCliente.getText());
        } catch (IOException ex) {
            Logger.getLogger(NovoCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
                       
        return JOptionPane.showOptionDialog(null, 
        texto, 
        tittle, 
        JOptionPane.YES_NO_OPTION, 
        JOptionPane.QUESTION_MESSAGE, 
        null, 
        jOpcoes, 
        jOpcoes[0]);
    }
    public void editaCliente() {
        String tittle = "Cadastro de Cliente";
        String texto = "Cadastro editado com sucesso!";
        
        try {
            Cliente.editaCliente( idEditar, nomeCliente.getText(), telefoneCliente.getText(), enderecoCliente.getText(), cpfCliente.getText() );
        } catch (IOException ex) {
            Logger.getLogger(NovoCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FaltaObjetosException ex) {
            Logger.getLogger(NovoCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(null, texto, tittle, JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void atualizaBotaoConfirmar() {
        if(cpfCliente.getText().isEmpty() || nomeCliente.getText().isEmpty() || telefoneCliente.getText().isEmpty() || enderecoCliente.getText().isEmpty()) {
            confirmar.setEnabled(false);
        } else {
            confirmar.setEnabled(true);
        }
    }

