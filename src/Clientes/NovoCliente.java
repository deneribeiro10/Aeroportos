    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        
        int yesOrNo = 0;
        
        if(criarOuEditar) {
            yesOrNo = criaCliente();
        } else {
            editaCliente();
        }
        Clientes.listaAtualizada();
        Clientes.atualizaLista();
        
        if(yesOrNo == 0 || yesOrNo == -1) { 
            try {
                this.setClosed(true);
            } catch (PropertyVetoException ex) {
                Logger.getLogger(NovoCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.nomeCliente.setText("");
            this.telefoneCliente.setText("");
            this.enderecoCliente.setText("");
            this.cpfCliente.setText("");
        }
        
        atualizaBotaoConfirmar();
    }//GEN-LAST:event_confirmarActionPerformed

