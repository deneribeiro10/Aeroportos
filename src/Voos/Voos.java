    private void removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerActionPerformed
        // TODO add your handling code here:
        
        int index = jList1.getSelectedIndex(); 
        int tamanho = dlm.getSize();
        
        int id = listaVoos.get(index).getId();
        
        if(0 !=  JOptionPane.showConfirmDialog(null, 
                "Um voo ser√° removido do sistema.\n Deseja continuar? ", 
                "Remover voo", 
                JOptionPane.YES_NO_OPTION
        )) {
            return;
        }
        
        dlm.remove(index); 
        listaVoos.remove(index);
        
        if(index == tamanho-1) {
            index--;
        }
        jList1.setSelectedIndex(index);
        jList1.ensureIndexIsVisible(index);
        
        try {
            Voo.removeVoo(id);
        } catch (IOException ex) {
            Logger.getLogger(Voos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (FaltaObjetosException ex) {
            Logger.getLogger(Voos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        Voos.listaAtualizada();
        Voos.atualizaLista();
        
    }//GEN-LAST:event_removerActionPerformed

    private void pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarActionPerformed
        // TODO add your handling code here:
        
        if(Voo.lista_voo.containsKey( Integer.parseInt( pesquisaTxt.getText() ) )) {
            
            dlm.removeAllElements();
            dlm.addElement( Voo.lista_voo.get( Integer.parseInt( pesquisaTxt.getText() ) ).getId()
            + " - " 
            + Voo.lista_voo.get( Integer.parseInt( pesquisaTxt.getText() ) ).getDataPartida() );        
            
            Voos.listaAtualizada(Voo.lista_voo.get( Integer.parseInt(pesquisaTxt.getText() ) ));
            
            this.notFound.setVisible(false);
            
        } else {
            
            dlm.removeAllElements();
            this.notFound.setVisible(true);
        }
        
    }//GEN-LAST:event_pesquisarActionPerformed

