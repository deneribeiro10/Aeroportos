    public static void listaAtualizada() {
        Voos.listaVoos.clear();
        
        try {
            Voo.lista_voo = Voo.getLista( CriaArquivo.read("voo") );
        } catch (IOException ex) {
            Logger.getLogger(Voos.class.getName()).log(Level.SEVERE, null, ex);
        }
                    
        Voos.listaVoos.addAll(Voo.lista_voo.values());
    }
    
    public static void listaAtualizada(Voo v) {
        Voos.listaVoos.clear();
        Voos.listaVoos.add(v);
    }
    
    public static void atualizaLista() {
        
        try {
            Voo.lista_voo = Voo.getLista( CriaArquivo.read("voo") );
        } catch (IOException ex) {
            Logger.getLogger(Voos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dlm.removeAllElements();
        
        listaVoos.forEach((voo) -> {
            dlm.addElement(String.valueOf( voo.getId() ) + " - " + voo.getDataPartida());
        });
        
    }
    
    private void botoes() {
        if(dlm.getSize() == 0 || jList1.getSelectedIndex() == -1) {
            remover.setEnabled(false);
            editar.setEnabled(false);
        } else {
            remover.setEnabled(true);
            editar.setEnabled(true);
        }
    }
    
    

